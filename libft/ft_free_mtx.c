#include "libft.h"

int		ft_free_mtx(char **mtx)
{
	int		i;

	i = 0;
	while (mtx[i] != NULL)
	{
		ft_memdel((void**)&mtx[i]);
		i++;
	}
	ft_memdel((void**)&mtx);
	return (1);
}
