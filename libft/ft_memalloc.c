#include "libft.h"

void	*ft_memalloc(size_t size)
{
	char	*p;
	size_t	i;

	i = 0;
	if ((p = malloc(size)))
	{
		while (i < size)
			p[i++] = '\0';
		return (p);
	}
	return (0);
}
